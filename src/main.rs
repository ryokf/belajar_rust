fn learn_05_data_type_boolean() {
    // tipe data boolean hanya memiliki dua nilai, yaitu true dan false
    // deklarasi tipe data boolean secara implisit
    let t = true;
    // deklarasi tipe data boolean secara eksplisit
    let f: bool = false;
    println!("t = {} \nf = {}", t, f);
}



